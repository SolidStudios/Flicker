package io.github.SolidStudiosTeam.Flicker.engine.block;

public class BlockTextureCoordinates {
	private float x, y;
	
	public BlockTextureCoordinates(float x, float y){
		this.x = x;
		this.y = y;
	}

	/**
	 * @return the x
	 */
	public float getX() {
		return x;
	}

	/**
	 * @return the y
	 */
	public float getY() {
		return y;
	}
}
